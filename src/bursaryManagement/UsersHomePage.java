    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Checking the Value selected and Opening the correspoinding JFrame form
        String wardSelected = jComboBoxWard.getSelectedItem().toString();
        if(wardSelected.equals("Biashara Ward")){
            setVisible(false);
            new BiasharaWard().setVisible(true);
        }
        else if(wardSelected.equals("Flamingo Ward")){
            setVisible(false);
            new FlamingoWard().setVisible(true);
        }
        else if(wardSelected.equals("Menengai Ward")){
            setVisible(false);
            new MenengaiWard().setVisible(true);
        }
        else if(wardSelected.equals("Nakuru East Ward")){
            setVisible(false);
            new NakuruEastWard().setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select your county assembly!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

