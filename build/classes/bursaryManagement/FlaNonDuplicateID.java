    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        ResultSet rs = SelectFromDB.getData("SELECT * FROM flaBursaryForm");
        DefaultTableModel model =(DefaultTableModel)flaform.getModel();
        model.setRowCount(0);
        try {
            while(rs.next()){
                model.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(5),rs.getString(4),rs.getString(6),rs.getString(7),rs.getString(10),rs.getString(11),rs.getString(19)});
            }
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formComponentShown

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String search = jTextFieldSearch.getText();
        if(search.isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter student name or id or school or gender or disability to find details!");
        }
        else{
            ResultSet rs = SelectFromDB.getData("SELECT * FROM flaBursaryForm WHERE studentName LIKE '%"+search+"%' or institutionName LIKE '%"+search+"%' or nationalID LIKE '%"+search+"%' or studentGender LIKE '%"+search+"%' or anyDisability LIKE '%"+search+"%'");
            DefaultTableModel model =(DefaultTableModel)flaform.getModel();
            model.setRowCount(0);
            try {
                 while(rs.next()){
                     model.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(5),rs.getString(4),rs.getString(6),rs.getString(7),rs.getString(10),rs.getString(11),rs.getString(19)});
                }
                rs.close();
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

